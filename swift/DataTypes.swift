// swift is a type-safe language

var myString = "String"
var myInt = 0
var myFloat = 0.123456
var myDouble = 0.123456789012345
var myBool = true;

print(myString)
print(myInt)
print(myFloat)
print(myDouble)
print(myBool)

// type annotation
var myNewString: String
var myNewInt: Int
var myNewFloat: Float
var myNewDouble: Double
var myNewBool: Bool

// Unnecessary
var myVar: String = "myVar"
