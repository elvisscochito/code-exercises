public class Declaraciones_operaciones
{
   public static void main(String[] args)
   {
      int a=10;
      int b=5;
      int suma=a+b;
      int resta=a-b;
      int multiplicacion=a*b;
      int division=a/b;
      
      System.out.println("La suma de"+a+"más" +b+ "es igual a" +suma);
      System.out.println("La resta de"+a+"menos"+b+"es igual a" +resta);
      System.out.println("La multiplicación de"+a+"por"+b+"es igual a" +multiplicacion);
      System.out.println("La división de"+a+"entre"+b+"es igual a" +division);
   }
}
    